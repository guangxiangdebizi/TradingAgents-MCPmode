    def _format_report_content(self, content: str) -> str:
        """Ê†ºÂºèÂåñÊä•ÂëäÂÜÖÂÆπ"""
        if not content:
            return "ÊöÇÊó†Êï∞ÊçÆ"
        
        # ÁÆÄÂçïÁöÑÊ†ºÂºèÂåñÂ§ÑÁêÜ
        lines = content.split('\n')
        formatted_lines = []
        
        for line in lines:
            line = line.strip()
            if not line:
                continue
            
            # Ê£ÄÊü•ÊòØÂê¶ÊòØÊ†áÈ¢òÊ†ºÂºè
            if line.endswith('Ôºö') or line.endswith(':'):
                formatted_lines.append(f"**{line}**")
            elif line.startswith('- ') or line.startswith('‚Ä¢ '):
                formatted_lines.append(line)
            elif line.startswith(('1.', '2.', '3.', '4.', '5.')):
                formatted_lines.append(line)
            else:
                formatted_lines.append(line)
        
        return "\n".join(formatted_lines)
    
    def _format_debate_content(self, debate_history: str) -> str:
        """Ê†ºÂºèÂåñËæ©ËÆ∫ÂÜÖÂÆπ"""
        if not debate_history:
            return "ÊöÇÊó†Ëæ©ËÆ∫ËÆ∞ÂΩï"
        
        # ÂàÜÂâ≤‰∏çÂêåÂèëË®ÄËÄÖÁöÑÂÜÖÂÆπ
        sections = debate_history.split('„Äê')
        formatted_sections = []
        
        for section in sections:
            if not section.strip():
                continue
            
            if '„Äë' in section:
                speaker, content = section.split('„Äë', 1)
                speaker = speaker.strip()
                content = content.strip()
                
                formatted_sections.append(f"### {speaker}")
                formatted_sections.append(content)
                formatted_sections.append("")
            else:
                formatted_sections.append(section.strip())
        
        return "\n".join(formatted_sections)
    
